# ✅ Built-in Data Structures:
"""
Python has several built-in data structures, which are the core containers used to store and manage data efficiently
"""

# 1️⃣ List:
"""
Ordered, mutable (changeable), allows duplicates.
Syntax: my_list = [1, 2, 3, "apple"]
my_list.append(40)  # [10, 20, 30, 40]
"""


# 2️⃣ Tuple:
"""
Ordered, immutable, allows duplicates.
Syntax: my_tuple = (1, 2, 3)
my_tuple = (10, 20, 30)
"""


# 3️⃣ Set:
"""
Unordered, mutable, no duplicate elements.
Syntax: my_set = {1, 2, 3}
my_set.add(4)
"""



# 4️⃣ Dictionary (dict):
"""
Key-value pairs, unordered (in versions < 3.7), mutable, keys must be unique.

Syntax:
my_dict = {
    "name": "John",
    "age": 30
}
my_dict["age"] = 26  # Update value

"""


